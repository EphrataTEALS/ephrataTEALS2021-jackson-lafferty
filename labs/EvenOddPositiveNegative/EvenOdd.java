package labs.EvenOddPositiveNegative;
public class EvenOdd {
    public static void main(String[] args) {
        int x = 10;
        if (x % 2 == 0) {
            System.out.println(x + " is an even number!");
        } else {
            System.out.println(x + " is and odd number!");
        }
    }
}